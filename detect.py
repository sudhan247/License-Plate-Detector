    opt_weights='weights/last.pt'
    opt_confthres=0.5
    opt_iouthres=0.5
    opt_fourcc='mp4v'
    opt_source='pics'
    opt_device=''
    opt_view_img='store_true'
    opt_save_txt='store_true'
    opt_classes=0
    opt_agnostic_nms='store_true'
    opt_augment='store_true'
    with torch.no_grad():
        detect()
